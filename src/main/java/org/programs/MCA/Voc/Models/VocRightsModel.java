package org.kaznalnrprograms.MCA.Voc.Models;

public class VocRightsModel {
    private String vocView;
    private String vocDel;
    private String vocChange;

    public String getVocView() {
        return vocView;
    }

    public void setVocView(String vocView) {
        this.vocView = vocView;
    }

    public String getVocDel() {
        return vocDel;
    }

    public void setVocDel(String vocDel) {
        this.vocDel = vocDel;
    }

    public String getVocChange() {
        return vocChange;
    }

    public void setVocChange(String vocChange) {
        this.vocChange = vocChange;
    }
}
